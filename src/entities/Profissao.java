package entities;

public class Profissao {
    private Integer idProfissao;
    private String nome;

    public Integer getIdProfissao() {
        return idProfissao;
    }

    public void setIdProfissao(Integer idProficao) {
        this.idProfissao = idProficao;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
