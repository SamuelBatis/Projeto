package entities;

public class Efermidade {
    private Integer efermidadeId;
    private String nome;

    public Integer getEfermidadeId() {
        return efermidadeId;
    }

    public void setEfermidadeId(Integer efermidadeId) {
        this.efermidadeId = efermidadeId;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
